const month = 6

switch (month) {
    case 1:
        console.log("jan");
        break; // stop the below execution in the scope except default
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("Apr");
        break;
    case 5:
        console.log("May");
        break;
    case 6:
        console.log("June");
        break;
    case 7:
        console.log("July");
        break;
    case 8:
        console.log("Aug");
        break;

    default:
        console.log("default case matched")
        break;
}
